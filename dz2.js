function findValueByKey(companyObject, companyName) {
  if (companyObject.name === companyName) {
    console.log("Найден объект:", companyObject);
  } else if (Array.isArray(companyObject.partners)) {
    companyObject.partners.forEach((partner) => {
      findValueByKey(partner, companyName);
    });
  } else if (Array.isArray(companyObject.clients)) {
    companyObject.clients.forEach((client) => {
      findValueByKey(client, companyName);
    });
  } 
};
